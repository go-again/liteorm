// Package gen is liteorm's code generator. It emits typed Column[V] constants
// (and, from DB introspection, model structs) so queries get compile-time COLUMN
// safety on top of the runtime-validated query.Col tokens:
//
//	query.Select[User](db).Filter(UserColumns.Name.Eq("alice"))
//
// Two input modes share one emitter:
//   - FromType[T]   — reflect over an existing Go model (the struct stays the
//     source of truth); emit just the columns.
//   - FromDB        — introspect a live database (via orm.IntrospectColumns) and
//     emit the model struct + columns for tables that have no Go type yet.
//
// gen is driver-free: FromDB takes a liteorm.Session, so the caller wires the
// backend. The "SQL → typed Go" path (a SQL parser) lives in the separate
// sqlc-gen-liteorm plugin.
package gen

import (
	"context"
	"fmt"
	"go/format"
	"io"
	"reflect"
	"strings"

	liteorm "liteorm.org"
	"liteorm.org/internal/scan"
	"liteorm.org/orm"
)

// Field is a column to generate (Go name, db column, the V for Column[V]).
type Field struct {
	GoName string
	Column string
	GoType string
}

// Model is a table's generated shape.
type Model struct {
	GoName string
	Table  string
	Fields []Field
}

// FromType derives a Model from an existing Go struct via reflection and the
// shared liteorm tag resolver. Pointer/sql.NullX field types map to their base.
func FromType[T any]() Model {
	t := reflect.TypeFor[T]()
	m := Model{GoName: t.Name(), Table: scan.TableNameOf(t)}
	walkFields(t, "", &m)
	return m
}

func walkFields(t reflect.Type, colPrefix string, m *Model) {
	for i := 0; i < t.NumField(); i++ {
		sf := t.Field(i)
		if !sf.IsExported() {
			continue
		}
		if ep, emb := scan.EmbeddedInfo(sf); emb {
			et := sf.Type
			for et.Kind() == reflect.Pointer {
				et = et.Elem()
			}
			walkFields(et, colPrefix+ep, m)
			continue
		}
		if scan.IsRelationField(sf.Type) {
			continue
		}
		ci := scan.ResolveColumn(sf)
		if ci.Skip {
			continue
		}
		m.Fields = append(m.Fields, Field{GoName: sf.Name, Column: colPrefix + ci.Name, GoType: scan.GoTypeName(sf.Type)})
	}
}

// FromDB introspects the given tables on a live session into Models, mapping each
// column's SQL type to a Go type.
func FromDB(ctx context.Context, sess liteorm.Session, tables ...string) ([]Model, error) {
	out := make([]Model, 0, len(tables))
	for _, tbl := range tables {
		cols, err := orm.IntrospectColumns(ctx, sess, tbl)
		if err != nil {
			return nil, err
		}
		if len(cols) == 0 {
			return nil, fmt.Errorf("gen: table %q not found or has no columns", tbl)
		}
		m := Model{GoName: GoName(tbl), Table: tbl}
		for _, c := range cols {
			m.Fields = append(m.Fields, Field{GoName: GoName(c.Name), Column: c.Name, GoType: sqlToGo(c.Type)})
		}
		out = append(out, m)
	}
	return out, nil
}

// WriteColumns emits typed Column[V] constants for each model — for models whose
// Go struct already exists.
func WriteColumns(w io.Writer, pkg string, models ...Model) error {
	return emit(w, pkg, models, false)
}

// WriteModels emits the struct + TableName + Column constants for each model —
// for DB-introspected models that have no Go type yet.
func WriteModels(w io.Writer, pkg string, models ...Model) error {
	return emit(w, pkg, models, true)
}

func emit(w io.Writer, pkg string, models []Model, withStruct bool) error {
	needTime := false
	for _, m := range models {
		for _, f := range m.Fields {
			if f.GoType == "time.Time" {
				needTime = true
			}
		}
	}
	var b strings.Builder
	b.WriteString("// Code generated by liteorm gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("import (\n")
	if needTime {
		b.WriteString("\t\"time\"\n\n")
	}
	b.WriteString("\t\"liteorm.org/query\"\n)\n\n")
	for _, m := range models {
		if withStruct {
			emitStruct(&b, m)
		}
		emitColumns(&b, m)
	}
	src, err := format.Source([]byte(b.String()))
	if err != nil {
		return fmt.Errorf("gen: formatting generated source: %w\n--- source ---\n%s", err, b.String())
	}
	_, err = w.Write(src)
	return err
}

func emitStruct(b *strings.Builder, m Model) {
	fmt.Fprintf(b, "// %s maps table %q.\n", m.GoName, m.Table)
	fmt.Fprintf(b, "type %s struct {\n", m.GoName)
	for _, f := range m.Fields {
		fmt.Fprintf(b, "\t%s %s `db:%q`\n", f.GoName, f.GoType, f.Column)
	}
	b.WriteString("}\n\n")
	fmt.Fprintf(b, "func (%s) TableName() string { return %q }\n\n", m.GoName, m.Table)
}

func emitColumns(b *strings.Builder, m Model) {
	fmt.Fprintf(b, "// %sColumns are typed, compile-time-safe columns for %s.\n", m.GoName, m.GoName)
	fmt.Fprintf(b, "var %sColumns = struct {\n", m.GoName)
	for _, f := range m.Fields {
		fmt.Fprintf(b, "\t%s query.Column[%s]\n", f.GoName, f.GoType)
	}
	b.WriteString("}{\n")
	for _, f := range m.Fields {
		fmt.Fprintf(b, "\t%s: query.Col[%s](%q),\n", f.GoName, f.GoType, f.Column)
	}
	b.WriteString("}\n\n")
}
